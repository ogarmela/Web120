Responsive vs Mobile Application
Name: Omar Garmela 
A responsive website adapts to the type of device being used to access it. For each visitor, the website will
automatically adjust the layout of its content to match the visitor’s screen size. For example, content may be displayed in three-column format for a user accessing the website via a desktop computer,  wo-column format for a user accessing the website via a tablet or phablet, or in one-column format for a user accessing the website using a smartphone.
A responsive website has the advantage of being
the best way to achieve parity of access to content for all visitors to your website, regardless of which device they are using to access it. This has obvious advantages in terms of the development and maintenance costs of
developing a single site vs the main site plus a separate dedicated mobile site, as well as simplifying the content management planning processes.
Which is Better – an App or a Mobile
(Responsive) Website?
When it comes to deciding whether to build a
native app or a mobile website, the most appropriate choice really depends on
your end goals. If you are developing an interactive game an app is probably
going to be your best option. But if your goal is to offer mobile-friendly
content to the widest possible audience then a mobile website is probably the
way to go. In some cases you may decide you need both a mobile website and a
mobile app, but it’s pretty safe to say that it rarely makes sense to build an
app without already having a mobile website in place.
Advantages of a Mobile Website vs. Native Apps
If your goals are primarily related to
marketing or public communications, a
Mobile/responsive website is almost always going to make sense as a practical first step in your mobile outreach strategy. This is because a mobile website has a number of inherent advantages over apps, including broader accessibility, compatibility and cost-effectiveness.
Immediacy
– Mobile Websites Are Instantly Available 
Compatibility
– Mobile Websites are Compatible Across Devices
A single mobile website can reach users across
many different types of mobile devices, whereas native apps require a separate
version to be developed for each type of Device.
 
 
 
 
 
 
 
 
 
 
 
 



